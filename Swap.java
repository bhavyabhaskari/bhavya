
public class Swap {

	public static void main(String[] args) {
		int num1,num2,c;
		num1=10;
		num2=20;
		System.out.println("numbers before swapping:" +num1+" and "+num2);
		
		 c=num1;
		 num1=num2;
		 num2=c;
		 System.out.println("numbers after swapping:"+num1+" and "+num2);

	}

}

